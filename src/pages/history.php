                <p>
                    Пензенский колледж архитектуры и строительства имеет богатую историю и традиции, занимает достойное место в ряду образовательных учреждений, обеспечивающих качественную подготовку квалифицированных специалистов для региона и страны.
                </p>
                <p>
                    А начиналось все в далеком 1944 году. Еще не отгремели бои Великой Отечественной войны, еще сотни городов и сел страны лежали в руинах, а в Пензе на улице Шевченко в здании школы № 14 по приказу Комитета обороны был открыт строительный техникум. Страна жила предчувствием Победы. Хотелось строить, созидать, возрождать… Профессия строителя становится одной из самых необходимых.
                    На 1 и 2 курсы отделения «Промышленное и гражданское строительство» были приняты 180 учащихся.
                </p>
                <p>
                    4 октября 1944 года прозвенел первый звонок, известивший о начале учебного года. Коллектив преподавателей насчитывал 12 человек. Первый директор техникума — Карлин С. С. был человек широкой образованности, окончивший горную академию. Главной задачей педагогического коллектива считал подготовку грамотных, компе­тентных в деле строительства специалистов.
                </p>
