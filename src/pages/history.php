                </p>
                <p>
                    Первый полный выпуск учащихся состоялся в 1948 году. Дипломы об окончании техникума вручались в том же первом здании на улице Шевченко. 65 юношей и девушек получили путевку в жизнь. Строители были нужны повсюду: восстанавливалось разрушенное войной
                </p>
                <p>
                    народное хозяйство, осваивались новые целинные земли, начиналась эпоха больших строек.
                </p>
                <p>
                    Выпускники Пензенского строительного техникума разъехались по всей стране — от Подмосковья до Сибири. И все они с честью вложили свой труд, свои знания, частицу своей души в избранное дело.
                    В 1948 году для учащихся и преподавателей произошло важное событие — закончился капи­тальный ремонт здания на улице Московской. Техникум обрел свое собственное здание, в которое вложили первый
                </p>
