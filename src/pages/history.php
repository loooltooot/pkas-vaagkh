                <p>
                    практический опыт строительства сами учащиеся. Работали с энтузиазмом, после занятий, во время производственной практики.
                </p>
                <p>
                    Строили для себя. В этом же году вместо отделения «Планирование строительства» было открыто новое отделение «Нормирование строительства». В это время возглавлял техникум директор Гаврилов Н. М. Контин­гент учащихся составлял 375 человек. На преподавательской работе были заняты 12 штатных преподавателей.
                </p>
                <p>
                    Контингент учащихся увеличивался. В 1952 году в техникуме было открыто вечернее отделение. Требовались дополнительные площади.
                    В 1955 году директором техникума становится Хрянин А. Г. Строятся мастерские и лаборатория строительных материалов.
                </p>
                <p>
                    В 1956 году открывается заочное отделение, а с 1957 года начинает работать отделение «Технология и изготовление железобетонных изделий».
                    В 1963 году в техникуме работают 2 отделения: «Промышленное и гражданское строительство» и «Технология железобетонных изделий». Возобновляется строительство нового учебного корпуса, которое закончилось в 1964 году. А с начала 1965 года техникум перешел в новое прекрасное четырехэтажное здание с хорошими актовым и спортивным залами, мастерскими, лабораториями. Появилась возможность перейти на кабинетную систему обучения, учащиеся получили прекрас­ную возможность заниматься спортом., техническим творчеством, художественной самодеятельно­стью.
                </p>
                <p>
                    В 1967 году закончено строительство нового пятиэтажного здания для общежития на 300 мест, оборудованное современной мебелью, с хорошими кухнями, просторным красным уголком.
                </p>
                <p>
                    В 1968 году в техникуме открывается новое отделение — «Архитектура». Эта специальность отметила в 2018 году 50-летний юбилей!
                </p>
                <p>
                    1969 год стал юбилейным для Пензенского строительного техникума. За 25 лет своего сущест­вования для страны было подготовлено 3478 специалистов — строителей, плановиков, нормировщиков и технологов.
                </p>
                <p>
                    В 1973 году директором техникума стал Ивачев М.В. Под его руководством построено студен­ческое общежитие № 2, где разместились столовая, буфет, медпункт, зубоврачебный и физиотера­певтический кабинеты.
                </p>
                <p>
                    В 1979 году Пензенский строительный техникум начинает подготовку специалистов для зарубеж­ных стран: Азии, Африки и Латинской Америки. Много сил и умений понадобилось преподавателям для того, чтобы научить иностранных учащихся основам специальных наук. За 15 лет отделение по обучению иностранных учащихся сделало 11 выпусков. Более 200 выпускников из Алжира, Конго, Анголы, Мали, Йемена, Чили, Лаоса, Афганистана, Монголии и многих других стран успешно работают в области строительства по избранным специальностям.
                </p>
                <p>
                    С 1985 года техникум возглавляет Чубарь С. В. Под его руководством создаются учебные кабинеты, оснащенные электронно-вычислительной техникой. Главной задачей техникума по-прежнему остается подготовка высококвалифицированных специалистов. В учебный процесс внедряются современные методики преподавания, улучшается учебно-методическая база, кабинеты оснащаются электронно-вычислительной техникой.
                </p>
                <p>
                    1991 год — новая веха в биографии учебного заведения. Техникум получил статус колледжа. Подготовка специалистов проходит по системе двух уровней — техник, младший инженер. Переход на новую систему обучения потребовал принципиально новых подходов к содержанию подготовки специалистов, более высокого уровня учебной и методической работы.
                    Происходящие в стране перемены изменили условия, в которых предстояло работать выпускникам. Преподавательский коллектив перешел на договорную систему подготовки специалистов. Создан и разработан новый курс обучения студентов по специальности «Коммерческая деятельность в строительстве».
                </p>
                <p>
                    1994 год. Свои 50 лет Пензенский строительный колледж прошел нелегкой дорогой. За годы становления приобретался ценный педагогический опыт, решались вопросы развития материально-технической базы, шел поиск в выборе успешных форм и методов учебного процесса.
                </p>
                <p>
                    За полвека Пензенский строительный колледж подготовил для страны более 11 тысяч специали­стов. 276 студентов получили дипломы с отличием. Обширна география российских городов, где работают выпускники Пензенского строительного колледжа. Творениями их рук украшены улицы городов: Москвы, Тамбова, Волгограда, Ульяновска, Пензы, Новосибирска, Калининграда, Тюмени, Алтайского края.
                </p>
                <p>
                    В 2002 г. директором колледжа становится А.Ю. Виноградов.
                    Колледж осуществляет подготовку по специальностям, отвечающим современным требованиям и приоритетам региона.
                </p>
                <p>
                    С 2016 года и по настоящее время директором колледжа является Спильная Елена Викторовна.
                    Сегодня «Пензенский колледж архитектуры и строительства» — это современное динамично развивающееся, имеющее достойный авторитет солидного, известного не только в регионе, но и в России, среднее профессиональное учебное заведение.
                    Разделы экспозиции музея рассказывают об истории создания колледжа, о преподавателях, первом директоре, а также об истории развития дополнительных занятий для студентов в колледже – спорте и других занятиях.
                </p>
