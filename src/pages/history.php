                <p>
                    Особенно трудно было иногородним — техникум не имел не только своего здания, но и своего общежития. Не было учебных кабинетов, не было библиотеки. Но было большое желание учиться!
                </p>
                <p>
                    Первый полный выпуск учащихся состоялся в 1948 году.
                </p>
                <p>
                    Выпускники Пензенского строительного техникума разъехались по всей стране — от Подмосковья до Сибири. И все они с честью вложили свой труд, свои знания, частицу своей души в избранное дело.
                    В 1948 году для учащихся и преподавателей произошло важное событие — закончился капи­тальный ремонт здания на улице Московской. Техникум обрел свое собственное здание, в которое вложили первый практический опыт строительства сами учащиеся. Работали с энтузиазмом, после занятий, во время производственной. практики.
                </p>
                <p>
                    В этом же году вместо отделения «Планирование строительства» было открыто новое отделение «Нормирование строительства». В это время возглавлял техникум директор Гаврилов Н. М. Контин­гент учащихся составлял 375 человек.
                </p>
                <p>
                    Контингент учащихся увеличивался. В 1952 году в техникуме было открыто вечернее отделение.
                </p>
                <p>
                    В 1955 году директором техникума становится Хрянин А. Г. Строятся мастерские и лаборатория строительных материалов.
                </p>
                <p>
                    В 1956 году открывается заочное отделение, а с 1957 года начинает работать отделение «Технология и изготовление железобетонных изделий».
                </p>
                <p>
                    С начала 1965 года техникум перешел в новое прекрасное четырехэтажное здание с хорошими актовым и спортивным залами, мастерскими, лабораториями. Появилась возможность перейти на кабинетную систему обучения.
                </p>
                <p>
                    В 1967 году закончено строительство нового пятиэтажного здания для общежития на 300 мест.
                </p>
                <p>
                    В 1968 году в техникуме открывается новое отделение — «Архитектура».
                </p>
                <p>
                    В 1973 году директором техникума стал Ивачев М.В. Под его руководством построено студен­ческое общежитие № 2.
                </p>
                <p>
                    В 1979 году Пензенский строительный техникум начинает подготовку специалистов для зарубеж­ных стран: Азии, Африки и Латинской Америки.
                </p>
                <p>
                    С 1985 года техникум возглавляет Чубарь С. В. Под его руководством создаются учебные кабинеты, оснащенные электронно-вычислительной техникой.
                </p>
                <p>
                    1991 год — новая веха в биографии учебного заведения. Техникум получил статус колледжа.
                </p>
                <p>
                    В 2002 г. директором колледжа становится А.Ю. Виноградов.
                </p>
                <p>
                    С 2016 года и по настоящее время директором колледжа является Спильная Елена Викторовна.
                </p>
